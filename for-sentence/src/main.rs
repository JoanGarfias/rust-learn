fn main() {
    /* BUCLE FOR
     * El bucle for itera sobre rangos de valores o las entradas de una colecci√≥n
     */


     println!("\nSintaxis: .. range");
     for x in 1..5 { //Del 1 al 4
         print!("{} ", x);
     }

     println!("\nSintaxis: array range");
     for elem in [1,2,3,4,5,6,7,8,9] {
         print!("{} ", elem);
     }
     println!();

}
